class ProductModel{
  String name;
  String imageUrl;
  int price;
  String manufacturer;
  String description;
  String color;
  int rating;
  String style;
  String madeIn;

  ProductModel({
    required this.name,
    required this.imageUrl,
    required this.price,
    required this.manufacturer,
    required this.description,
    required this.color,
    required this.rating,
    required this.style,
    required this.madeIn,
  });
}

